class AircraftSpawnEvent:

	def __init__(self, time, spawnpoint, destination):
		self.time = time
		self.spawnpoint = spawnpoint
		self.destination = destination

	def getTime(self):
		return time

	def getSpawnPoint(self):
		return spawnpoint

	def getDestination(self):
		return destination
